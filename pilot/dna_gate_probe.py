def cell_key(r):
    # role included so a hazard rep and a benign rep with the same id (none
    # share id today, but the v2 design keys on role explicitly) never collide.
    return (r["target_id"], r.get("role", "?"), r["framing"], r["rep"])


def load_ckpt(p):
    """Read JSONL ckpt; back-fill role+pair for v1 records (pre-v2 schema)
    so they merge cleanly with v2 records under the same analysis."""
    HAZ_MAP = {tid: (role, pair) for (tid, _n, role, pair, _e) in TARGETS_HAZARD}
    BEN_MAP = {tid: (role, pair) for (tid, _n, role, pair, _e) in TARGETS_BENIGN}
    done = {}
    if not p.exists():
        return done
    with open(p) as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                r = json.loads(line)
            except Exception:
                continue
            if "role" not in r:
                tid = r.get("target_id")
                if tid in HAZ_MAP:
                    r["role"], r["pair"] = HAZ_MAP[tid]
                elif tid in BEN_MAP:
                    r["role"], r["pair"] = BEN_MAP[tid]
            # back-fill over_flag for v1 records lacking the field
            if r.get("status") == "ok" and "over_flag" not in r:
                r["over_flag"] = bool(r.get("acknowledged_framework")) and not r.get("benign_appropriate_hits")
            done[cell_key(r)] = r
    return done


def _targets_for_mode(mode):
    if mode == "hazard":
        return TARGETS_HAZARD
    if mode == "benign":
        return TARGETS_BENIGN
    return TARGETS_HAZARD + TARGETS_BENIGN  # "both"


def enumerate_cells(reps, mode="hazard"):
    out = []
    for (tid, tname, role, pair, expected) in _targets_for_mode(mode):
        for fkey in FRAMINGS:
            for rep in range(1, reps + 1):
                out.append((tid, tname, role, pair, expected, fkey, rep))
    return out


