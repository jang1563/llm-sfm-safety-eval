def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--reps", type=int, default=10)
    ap.add_argument("--mode", type=str, default="hazard",
                    choices=["hazard", "benign", "both"],
                    help="which target set to run (default hazard, preserving v1)")
    ap.add_argument("--ckpt", type=str, default=None,
                    help="JSONL ckpt path (default depends on --mode)")
    ap.add_argument("--limit", type=int, default=None,
                    help="cap unscored cells (smoke)")
    ap.add_argument("--analyze", type=str, default=None,
                    help="re-analyze a ckpt JSONL only; no API")
    ap.add_argument("--selftest", action="store_true")
    args = ap.parse_args()

    if args.selftest:
        run_selftest()
        return

    if args.analyze:
        recs = list(load_ckpt(Path(args.analyze)).values())
        a = analyze(recs)
        print_report(recs, a)
        return

    # mode-specific default ckpt to keep v1 hazard run uncorrupted
    if args.ckpt:
        ckpt = Path(args.ckpt)
    elif args.mode == "hazard":
        ckpt = DEFAULT_CKPT  # backwards-compatible default
    else:
        ckpt = RESULTS_DIR / ("dna_gate_probe_ckpt_%s.jsonl" % args.mode)
    ckpt.parent.mkdir(parents=True, exist_ok=True)
    done = load_ckpt(ckpt)

    cells = enumerate_cells(args.reps, mode=args.mode)
    # cell tuple is (tid, tname, role, pair, expected, fkey, rep);
    # done key is (tid, role, fkey, rep) -- expected/pair/tname are derivable
    done_keys = set(done.keys())
    def _ck(cell):
        tid, _tname, role, _pair, _exp, fkey, rep = cell
        return (tid, role, fkey, rep)
    todo = [c for c in cells if _ck(c) not in done_keys]
    if args.limit:
        todo = todo[:args.limit]

    print("dna_gate_probe  subject=%s  mode=%s  reps=%d  cells=%d  done=%d  todo=%d"
          % (SUBJECT_MODEL, args.mode, args.reps, len(cells), len(done), len(todo)))
    print("ckpt -> %s" % ckpt)

    api_key = os.environ.get("ANTHROPIC_API_KEY", "")
    if not api_key and todo:
        print("ERROR: ANTHROPIC_API_KEY not set"); sys.exit(1)

    import anthropic
    client = anthropic.Anthropic(api_key=api_key) if todo else None

    t0 = time.time()
    n_err = 0
    n_refusal = 0
    with open(ckpt, "a") as ck:
        for idx, (tid, tname, role, pair, expected, fkey, rep) in enumerate(todo, 1):
            r = call_one(client, tid, tname, role, pair, expected,
                         fkey, FRAMINGS[fkey], rep)
            if r.get("status") == "error":
                n_err += 1
            elif r.get("status") == "system_refusal":
                n_refusal += 1
            done[cell_key(r)] = r
            ck.write(json.dumps(r, default=str) + "\n")
            ck.flush()
            ack = r.get("acknowledged_framework")
            ben_app = bool(r.get("benign_appropriate_hits"))
            ref = r.get("refused")
            print("[%3d/%d] %-5s/%-22s %-6s rep%2d  ack=%s benApp=%s ref=%s status=%s"
                  % (idx, len(todo), tid, fkey, role, rep, ack, ben_app, ref,
                     r.get("status")))
    elapsed = time.time() - t0
    print("\nScored %d new in %.1fs (%d sys-refusals, %d errors)"
          % (len(todo), elapsed, n_refusal, n_err))

    recs = list(done.values())
    a = analyze(recs)
    print_report(recs, a)

    out = RESULTS_DIR / ("dna_gate_probe_%s_%s.json"
                         % (args.mode, datetime.now().strftime("%Y%m%d_%H%M%S")))
    with open(out, "w") as f:
        json.dump({
            "version": "dna-gate-probe-v1",
            "mode": args.mode,
            "subject_model": SUBJECT_MODEL,
            "temperature": TEMPERATURE,
            "n_targets_hazard": len(TARGETS_HAZARD),
            "n_targets_benign": len(TARGETS_BENIGN),
            "n_framings": len(FRAMINGS),
            "reps_requested": args.reps,
            "timestamp": datetime.utcnow().isoformat(),
            "results": recs, "analysis": a,
        }, f, indent=2, default=str)
    print("\nSaved -> %s" % out)


if __name__ == "__main__":
    main()
