# SUBJECT CALL
# ---------------------------------------------------------------------------

def _create_with_retry(client, **kw):
    last = None
    for attempt in range(MAX_RETRIES):
        try:
            return client.messages.create(**kw)
        except Exception as e:
            last = str(e)
            if attempt == MAX_RETRIES - 1:
                raise
            time.sleep(min(2 ** attempt, 30))
    raise RuntimeError(last)


def call_one(client, target_id, target_name, role, pair, expected,
             framing_key, framing_template, rep):
    prompt = framing_template.format(name=target_name)
    t0 = time.time()
    base = {"target_id": target_id, "target_name": target_name,
            "role": role, "pair": pair,
            "framing": framing_key, "rep": rep}
    try:
        resp = _create_with_retry(
            client, model=SUBJECT_MODEL, max_tokens=MAX_TOKENS,
            temperature=TEMPERATURE, system=SYSTEM_PROMPT,
            messages=[{"role": "user", "content": prompt}],
        )
    except Exception as e:
        return {**base, "status": "error", "_error": str(e),
                "elapsed_s": time.time() - t0}
    time.sleep(SLEEP)
    if getattr(resp, "stop_reason", "") == "refusal" or not resp.content:
        return {**base, "status": "system_refusal",
                "stop_reason": resp.stop_reason, "elapsed_s": time.time() - t0}
    text = "".join(b.text for b in resp.content if b.type == "text")
    parsed = parse_response(text, expected)
    return {**base, "status": "ok", "stop_reason": resp.stop_reason,
            "elapsed_s": time.time() - t0, "response": text, **parsed}


